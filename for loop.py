# for loop: repeating n times
# requires you to define a start and stop point. Steps can be positive or negative.



for counter in range (1,11, 1):
    print("hello", counter)
# for numbers between 1 to 10 add 1 and print

# for loop to print 10 to 1

for x in range(10, 0, -1):
    print("hello", x)


# printing from -9 to 9

for i in range(-9, 10, 1):
    print("hi", i)

# from 9 to -9
for z in range(10, -10, -1):
    print("yay", z)
